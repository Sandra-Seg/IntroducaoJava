public class OperaAritimeticas {
    public static void main(String[] args) {
        int numero1 = 10;
        int numero2 = 5;


        int subtracao = numero1 - numero2;
        int soma = numero1 + numero2;
        int divisao = numero1 / numero2;
        int produto = numero1 * numero2;

        System.out.println("A subtracao de " + numero1 + " - " + numero2 + " é: " + subtracao);
        System.out.println("A soma de " + numero1 + " + " + numero2 + " é: " + soma);
        System.out.println("A divisao de " + numero1 + " / " + numero2 + " é: " + divisao);
        System.out.println("o produto de " + numero1 + " * " + numero2 + " é: " + produto);


    }

}

